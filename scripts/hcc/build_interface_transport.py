#!/usr/bin/env python3
"""
build_interface_transport.py — Interface Geometric Transport Analysis
======================================================================
Treats each patient's tumor–immune interface as a metric measure space
(I, d, μ) and computes pre→post therapy transport distances.

Three transport metrics per patient:
──────────────────────────────────────────────────────────────────────
1. WASSERSTEIN-2 DISTANCE  W₂(μ_pre, μ_post)
   Measures how much coexact "mass" was geometrically redistributed.
   Large W₂: active spatial reorganization of non-gradient structure.
   Small W₂: mass stayed in place (preservation or uniform collapse).

2. TRAJECTORY IN FEATURE SPACE  ΔX = X_post - X_pre
   X = (LCC, Gini, P95_norm, CoexactFrac, SpecEntropy)
   Tests whether R and NR therapy trajectories are directionally coherent
   — the Lipschitz question operationalised.

3. PERSISTENT TOPOLOGY  (via gudhi, Vietoris-Rips on interface nodes)
   H0: birth/death of connected coexact density clusters
   H1: persistent topological loops (circulation topology)
   Computes:
     - max H0 persistence (most robust hotspot cluster lifetime)
     - total H1 persistence (circulation topology, sum of loop lifetimes)
     - n_H1_persistent: number of loops surviving > persistence_threshold

Mathematical object
───────────────────
For each quality-filtered sample:
  coords_I = spatial coordinates of interface nodes
  mu_I     = coexact density c_i at interface nodes, normalised to sum=1

W₂ is computed via entropic regularised optimal transport (POT library).
Persistent homology via gudhi.RipsComplex on the spatial distance matrix.

Inputs
──────
results_hcc_coexact_hotspots.csv — for feature space vectors
data/hepatocellular_carcinoma/hcc_*_coexact.h5ad — for spatial data
  (generated by build_coexact_spatial.py; must be run first)

Outputs
───────
results_hcc_transport.csv          per-patient W2 and trajectory metrics
results_hcc_persistence.csv        per-sample persistence diagram summary
fig_hcc_trajectory_space.png       pre→post arrows in (LCC, CoexactFrac) space
fig_hcc_wasserstein_comparison.png W₂ by response group
"""

