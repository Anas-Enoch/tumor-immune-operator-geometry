def main():
    ap = argparse.ArgumentParser(
        description='Interface geometric transport analysis')
    ap.add_argument('--hotspot-csv',   default='results_hcc_coexact_hotspots.csv')
    ap.add_argument('--hodge-csv',     default='results_hcc_hodge_interface_summary.csv')
    ap.add_argument('--h5ad-dir',      default='data/hepatocellular_carcinoma')
    ap.add_argument('--ot-reg',        type=float, default=0.05,
                    help='Sinkhorn entropic regularisation (default 0.05)')
    ap.add_argument('--persist-thresh',type=float, default=0.05,
                    help='Persistence threshold for H0/H1 counting')
    ap.add_argument('--out-transport', default='results_hcc_transport.csv')
    ap.add_argument('--out-persist',   default='results_hcc_persistence.csv')
    ap.add_argument('--fig-traj',      default='fig_hcc_trajectory_space.png')
    ap.add_argument('--fig-wass',      default='fig_hcc_wasserstein_comparison.png')
    args = ap.parse_args()

    hot  = pd.read_csv(args.hotspot_csv)
    # Load Hodge interface summary for coexact_fraction
    try:
        hodge = pd.read_csv(args.hodge_csv)
        hodge_lookup = hodge.set_index('sample_id')
    except Exception:
        hodge_lookup = pd.DataFrame()

    ok   = hot[~hot['status'].isin(EXCLUDE)].copy()
    ok['cytassist_id'] = ok['sample_id'].str.extract(r'(cytassist_\d+)')
    ok['timepoint']    = ok['sample_id'].str.extract(r'(pre|post)$')

    # Add coexact_fraction from hodge summary if available
    if 'coexact_fraction' not in ok.columns and not hodge_lookup.empty:
        ok['coexact_fraction'] = ok['sample_id'].map(
            hodge_lookup.get('coexact_fraction', pd.Series(dtype=float)))

    # P95 scale for normalisation (cohort 95th percentile)
    p95_scale = float(ok['hotspot_p95'].quantile(0.95)) or 1.

    h5ad_dir  = Path(args.h5ad_dir)
    transport_rows = []
    persist_rows   = []

    print(f"\nProcessing paired patients …")
    for cid, grp in ok.groupby('cytassist_id'):
        pid, resp = PATIENT_MAP.get(cid, (None, None))
        pre_rows  = grp[grp['timepoint'] == 'pre']
        post_rows = grp[grp['timepoint'] == 'post']
        if len(pre_rows) == 0 or len(post_rows) == 0:
            continue

        pre_sid  = pre_rows.iloc[0]['sample_id']
        post_sid = post_rows.iloc[0]['sample_id']
        print(f"\n── {cid}  ({resp}) ──")

        # Load h5ad files
        pre_h5  = h5ad_dir / f'hcc_{pre_sid}_coexact.h5ad'
        post_h5 = h5ad_dir / f'hcc_{post_sid}_coexact.h5ad'
        if not pre_h5.exists() or not post_h5.exists():
            print(f"  SKIP — h5ad missing ({pre_h5.exists()} {post_h5.exists()})")
            continue

        pre_adata  = sc.read_h5ad(pre_h5)
        post_adata = sc.read_h5ad(post_h5)

        # Extract interface nodes and coexact density
        for label, adata in [('pre', pre_adata), ('post', post_adata)]:
            iface_mask   = adata.obs['interface_mask'].values.astype(bool)
            coords_iface = adata.obsm['spatial'][iface_mask]
            density_iface = adata.obs['coexact_density'].values[iface_mask]
            n_iface = int(iface_mask.sum())
            print(f"  {label}: n_interface={n_iface}  "
                  f"density_max={density_iface.max():.2f}")

            if n_iface >= 4 and density_iface.sum() > 1e-8:
                pt = persistent_topology(coords_iface, density_iface,
                                         persistence_threshold=args.persist_thresh)
                persist_rows.append(dict(
                    sample_id=f'hcc_{pre_sid if label=="pre" else post_sid}',
                    cytassist_id=cid, timepoint=label,
                    patient_id=pid, Response=resp,
                    n_interface=n_iface, **pt))
                print(f"    H0 max_pers={pt['max_h0_persistence']:.3f}  "
                      f"H1 max_pers={pt['max_h1_persistence']:.3f}  "
                      f"n_H1_persist={pt['n_h1_persistent']}")

        # Wasserstein distance pre→post
        iface_pre  = pre_adata.obs['interface_mask'].values.astype(bool)
        iface_post = post_adata.obs['interface_mask'].values.astype(bool)
        coords_pre  = np.ascontiguousarray(
            pre_adata.obsm['spatial'][iface_pre],  dtype=np.float64)
        coords_post = np.ascontiguousarray(
            post_adata.obsm['spatial'][iface_post], dtype=np.float64)
        den_pre  = np.asarray(
            pre_adata.obs['coexact_density'].values[iface_pre],  dtype=np.float64)
        den_post = np.asarray(
            post_adata.obs['coexact_density'].values[iface_post], dtype=np.float64)

        w2 = np.nan
        if (len(den_pre) >= 4 and len(den_post) >= 4 and
                den_pre.sum() > 1e-8 and den_post.sum() > 1e-8):
            w2 = wasserstein2_interface(coords_pre, den_pre,
                                        coords_post, den_post,
                                        reg=args.ot_reg)
            print(f"  W₂(pre→post) = {w2:.4f}")

        # Feature space trajectory
        X_pre  = feature_vector(pre_rows.iloc[0],  p95_scale)
        X_post = feature_vector(post_rows.iloc[0], p95_scale)
        delta_X    = X_post - X_pre
        traj_norm  = float(np.linalg.norm(delta_X[np.isfinite(delta_X)]))
        traj_angle = float(np.degrees(np.arctan2(
            delta_X[0] if np.isfinite(delta_X[0]) else 0,   # ΔLCC
            delta_X[3] if np.isfinite(delta_X[3]) else 0,   # ΔCoexactFrac
        )))

        transport_rows.append(dict(
            cytassist_id     = cid,
            patient_id       = pid,
            Response         = resp,
            n_pre            = int(pre_rows.iloc[0]['n_interface']),
            n_post           = int(post_rows.iloc[0]['n_interface']),
            wasserstein2     = w2,
            trajectory_norm  = traj_norm,
            trajectory_angle = traj_angle,
            delta_lcc        = float(delta_X[0]) if np.isfinite(delta_X[0]) else np.nan,
            delta_gini       = float(delta_X[1]) if np.isfinite(delta_X[1]) else np.nan,
            delta_coexact_frac = float(delta_X[3]) if np.isfinite(delta_X[3]) else np.nan,
            delta_spec_entropy = float(delta_X[4]) if np.isfinite(delta_X[4]) else np.nan,
            spatial_score_pre  = float(pre_rows.iloc[0]['hotspot_gini']
                                       * pre_rows.iloc[0]['largest_component_fraction']),
            spatial_score_post = float(post_rows.iloc[0]['hotspot_gini']
                                       * post_rows.iloc[0]['largest_component_fraction']),
        ))

    transport_df = pd.DataFrame(transport_rows)
    persist_df   = pd.DataFrame(persist_rows)

    transport_df.to_csv(args.out_transport, index=False)
    persist_df.to_csv(args.out_persist,    index=False)
    print(f'\nSaved → {args.out_transport}')
    print(f'Saved → {args.out_persist}')

    # ── Figures ───────────────────────────────────────────────────────────
    plot_trajectory(transport_df, hot, Path(args.fig_traj))
    plot_wasserstein(transport_df, Path(args.fig_wass))
    print(f'Saved → {args.fig_traj}')
    print(f'Saved → {args.fig_wass}')

    # ── Statistics ────────────────────────────────────────────────────────
    print('\n' + '='*65)
    print('TRANSPORT METRICS (paired patients)')
    print('='*65)
    print(transport_df[[
        'cytassist_id','Response','n_pre','n_post',
        'wasserstein2','trajectory_norm','delta_lcc','delta_coexact_frac'
    ]].to_string(index=False))

    for col, label in [('wasserstein2','W₂'), ('trajectory_norm','||ΔX||')]:
        r  = transport_df[transport_df['Response']=='Responder'][col].dropna()
        nr = transport_df[transport_df['Response']=='Non_Responder'][col].dropna()
        if len(r) > 0 and len(nr) > 0:
            _, p2 = mannwhitneyu(r, nr, alternative='two-sided')
            print(f'\n  {label}: R median={r.median():.4f}  NR median={nr.median():.4f}  '
                  f'p(two-sided)={p2:.4f}')

    print('\n' + '='*65)
    print('PERSISTENCE SUMMARY')
    print('='*65)
    if not persist_df.empty:
        print(persist_df[[
            'cytassist_id','timepoint','Response','n_interface',
            'max_h0_persistence','max_h1_persistence','n_h1_persistent'
        ]].to_string(index=False))

        for tp in ['pre','post']:
            sub = persist_df[persist_df['timepoint']==tp]
            r   = sub[sub['Response']=='Responder']['max_h1_persistence'].dropna()
            nr  = sub[sub['Response']=='Non_Responder']['max_h1_persistence'].dropna()
            if len(r) > 0 and len(nr) > 0:
                print(f'\n  H1 max persistence ({tp}):  '
                      f'R median={r.median():.3f}  NR median={nr.median():.3f}')


if __name__ == '__main__':
    main()
