def _make_rips(coords: np.ndarray, max_edge_length: float):
    """
    Compatibility wrapper for gudhi.RipsComplex.
    Handles two incompatible API versions:
      Keyword-only  (most builds):  RipsComplex(points=..., max_edge_length=...)
      Positional-only (some builds, e.g. Python 3.14):
                                    RipsComplex(pts, max_edge_length, False)
    """
    import inspect
    pts = np.ascontiguousarray(coords, dtype=np.float64)
    sig = inspect.signature(gudhi.RipsComplex.__init__)
    if 'points' in sig.parameters:
        return gudhi.RipsComplex(points=pts, max_edge_length=max_edge_length)
    # Positional-only: third arg is sparse_flag (False = full Rips)
    return gudhi.RipsComplex(pts, max_edge_length, False)
EXCLUDE = {'small_interface', 'low_tumor_signal', 'subgraph_too_small',
           'too_few_spots', 'low_tumor', 'skip'}


# ══════════════════════════════════════════════════════════════════════════════
# WASSERSTEIN DISTANCE
# ══════════════════════════════════════════════════════════════════════════════

def wasserstein2_interface(coords_pre: np.ndarray, mu_pre: np.ndarray,
                           coords_post: np.ndarray, mu_post: np.ndarray,
                           reg: float = 0.1) -> float:
    """
    Entropic-regularised W₂ between two interface coexact density measures.

    coords: (n, 2) spatial coordinates of interface nodes
    mu:     (n,)  coexact density, will be normalised to sum=1

    Uses POT (Python Optimal Transport) entropic OT for efficiency.
    reg: entropic regularisation (higher = faster, less accurate).
    """
    # Force contiguous float64 arrays (POT requires proper numpy arrays)
    coords_pre  = np.ascontiguousarray(coords_pre,  dtype=np.float64)
    coords_post = np.ascontiguousarray(coords_post, dtype=np.float64)

    # Normalise to probability measures
    a = np.abs(np.asarray(mu_pre,  dtype=np.float64))
    b = np.abs(np.asarray(mu_post, dtype=np.float64))
    if a.sum() < 1e-12 or b.sum() < 1e-12:
        return np.nan
    a /= a.sum(); b /= b.sum()

    # Squared Euclidean cost matrix (W₂ uses squared distances)
    M = ot.dist(coords_pre, coords_post, metric='sqeuclidean')
    # Normalise by median to make scale-invariant
    med = np.median(M[M > 0]) if (M > 0).any() else 1.
    M  /= med

    # Entropic OT (Sinkhorn)
    try:
        result = ot.sinkhorn2(a, b, M, reg=reg)
        W2 = float(result) if np.isscalar(result) else float(result[0])
    except Exception:
        W2 = np.nan
    return W2


# ══════════════════════════════════════════════════════════════════════════════
# PERSISTENT HOMOLOGY
# ══════════════════════════════════════════════════════════════════════════════

def persistent_topology(coords: np.ndarray, density: np.ndarray,
                        max_edge_length: float | None = None,
                        persistence_threshold: float = 0.05
                        ) -> dict:
    """
    Vietoris-Rips persistent homology on interface node positions,
    weighted by coexact density.

    The filtration value for an edge is its spatial length.
    The 'birth' of a H0 class corresponds to a spatial cluster forming;
    the 'death' corresponds to that cluster merging with another.

    Returns summary statistics of H0 and H1 persistence diagrams.
    """
    n = len(coords)
    if n < 4:
        return dict(max_h0_persistence=np.nan, sum_h0_persistence=np.nan,
                    n_h0_persistent=0, max_h1_persistence=np.nan,
                    sum_h1_persistence=np.nan, n_h1_persistent=0)

    # Auto-set max edge length as 2× median nearest-neighbour distance
    if max_edge_length is None:
        from sklearn.neighbors import NearestNeighbors
        nbrs = NearestNeighbors(n_neighbors=2).fit(coords)
        dists, _ = nbrs.kneighbors(coords)
        max_edge_length = float(np.median(dists[:, 1]) * 6)

    rips = _make_rips(coords, max_edge_length)
    st   = rips.create_simplex_tree(max_dimension=2)
    st.compute_persistence()
    diag = st.persistence()

    h0 = [(birth, death) for (dim, (birth, death)) in diag
          if dim == 0 and np.isfinite(death)]
    h1 = [(birth, death) for (dim, (birth, death)) in diag
          if dim == 1 and np.isfinite(death)]

    h0_pers = [d - b for b, d in h0]
    h1_pers = [d - b for b, d in h1]

    return dict(
        max_h0_persistence   = float(max(h0_pers)) if h0_pers else 0.,
        sum_h0_persistence   = float(sum(h0_pers)) if h0_pers else 0.,
        n_h0_persistent      = int(sum(1 for p in h0_pers
                                       if p > persistence_threshold)),
        max_h1_persistence   = float(max(h1_pers)) if h1_pers else 0.,
        sum_h1_persistence   = float(sum(h1_pers)) if h1_pers else 0.,
        n_h1_persistent      = int(sum(1 for p in h1_pers
                                       if p > persistence_threshold)),
    )


# ══════════════════════════════════════════════════════════════════════════════
# FEATURE SPACE TRAJECTORY
# ══════════════════════════════════════════════════════════════════════════════

def feature_vector(row: pd.Series, p95_scale: float = 1.) -> np.ndarray:
    """
    X = (LCC, Gini, P95_scaled, CoexactFrac, SpecEntropy)
    Returns NaN vector if any metric is missing.
    """
    try:
        return np.array([
            float(row.get('largest_component_fraction', np.nan)),
            float(row.get('hotspot_gini',               np.nan)),
            float(row.get('hotspot_p95',                np.nan)) / p95_scale,
            float(row.get('coexact_fraction',           np.nan)),
            float(row.get('spec_spectral_entropy',      np.nan)),
        ])
    except Exception:
        return np.full(5, np.nan)


