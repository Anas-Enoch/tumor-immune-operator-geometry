def plot_trajectory(transport_df: pd.DataFrame, hotspot_df: pd.DataFrame,
                    outpath: Path) -> None:
    """
    Pre→post arrows in (LCC, CoexactFrac) space and (Gini, LCC) space.
    """
    fig, axes = plt.subplots(1, 2, figsize=(13, 5))
    fig.suptitle('Interface geometry trajectories: pre→post therapy\n'
                 '(arrows show direction of change per patient)',
                 fontsize=11)

    ok = hotspot_df[~hotspot_df['status'].isin(EXCLUDE)].copy()

    for ax, (xcol, ycol, xlabel, ylabel) in zip(axes, [
        ('largest_component_fraction', 'coexact_fraction',
         'LCC fraction', 'Coexact fraction'),
        ('hotspot_gini', 'largest_component_fraction',
         'Gini', 'LCC fraction'),
    ]):
        pre  = ok[ok['timepoint'] == 'pre'].set_index('cytassist_id')
        post = ok[ok['timepoint'] == 'post'].set_index('cytassist_id')

        for cid in pre.index:
            if cid not in post.index: continue
            pr, po = pre.loc[cid], post.loc[cid]
            if xcol not in pr or ycol not in pr: continue
            resp = pr.get('response_group', 'Unknown')
            col  = COLOURS.get(resp, 'grey')
            xp, yp = float(pr[xcol]), float(pr[ycol])
            xo, yo = float(po[xcol]), float(po[ycol])
            if not all(np.isfinite([xp, yp, xo, yo])): continue

            ax.annotate('', xy=(xo, yo), xytext=(xp, yp),
                        arrowprops=dict(arrowstyle='->', color=col, lw=1.5))
            ax.scatter([xp], [yp], color=col, s=60, zorder=3)
            ax.text(xp - 0.01, yp + 0.005,
                    cid.replace('cytassist_', ''),
                    fontsize=7, color=col, ha='right')

        ax.set_xlabel(xlabel, fontsize=10); ax.set_ylabel(ylabel, fontsize=10)
        ax.axhline(0, color='grey', lw=0.5, ls='--')
        ax.axvline(0, color='grey', lw=0.5, ls='--')

    fig.legend(handles=[
        mpatches.Patch(color='#d62728', label='Responder'),
        mpatches.Patch(color='#1f77b4', label='Non-Responder'),
    ], fontsize=9, loc='lower center', ncol=2, bbox_to_anchor=(0.5, -0.04))
    plt.tight_layout()
    plt.savefig(outpath, dpi=180, bbox_inches='tight')
    plt.close()


def plot_wasserstein(transport_df: pd.DataFrame, outpath: Path) -> None:
    fig, axes = plt.subplots(1, 2, figsize=(12, 5))
    fig.suptitle('Interface transport geometry: W₂ and trajectory displacement\n'
                 '(quality-filtered pairs only)', fontsize=11)

    rng = np.random.RandomState(42)
    for ax, col, ylabel in [
        (axes[0], 'wasserstein2',
         'W₂ (entropic OT, normalised)\nlarger = more spatial redistribution'),
        (axes[1], 'trajectory_norm',
         '||ΔX|| in feature space\nlarger = more geometric change'),
    ]:
        for xi, (resp, c) in enumerate([
            ('Responder','#d62728'), ('Non_Responder','#1f77b4')
        ]):
            grp = transport_df[transport_df['Response'] == resp][col].dropna()
            if len(grp) == 0: continue
            jx  = xi + rng.uniform(-0.18, 0.18, len(grp))
            ax.scatter(jx, grp.values, color=c, s=80, zorder=3, alpha=0.9)
            ax.plot([xi-.3, xi+.3], [grp.median()]*2, color=c, lw=2.5, zorder=4)
            for i, (_, row) in enumerate(
                transport_df[transport_df['Response']==resp].dropna(subset=[col]).iterrows()
            ):
                ax.text(xi+.22, row[col],
                        row['cytassist_id'].replace('cytassist_',''),
                        fontsize=7, va='center', color=c)

        ax.axhline(0, color='grey', lw=0.5, ls='--')
        ax.set_xticks([0,1])
        ax.set_xticklabels(['Responder','Non-Responder'], fontsize=10)
        ax.set_ylabel(ylabel, fontsize=9)

        r_v  = transport_df[transport_df['Response']=='Responder'][col].dropna()
        nr_v = transport_df[transport_df['Response']=='Non_Responder'][col].dropna()
        if len(r_v) > 0 and len(nr_v) > 0:
            _, p = mannwhitneyu(r_v, nr_v, alternative='two-sided')
            ax.set_title(f'Mann-Whitney p = {p:.3f}', fontsize=9)

    fig.legend(handles=[
        mpatches.Patch(color='#d62728', label='Responder'),
        mpatches.Patch(color='#1f77b4', label='Non-Responder'),
    ], fontsize=9, loc='lower center', ncol=2, bbox_to_anchor=(0.5,-0.04))
    plt.tight_layout()
    plt.savefig(outpath, dpi=180, bbox_inches='tight')
    plt.close()


